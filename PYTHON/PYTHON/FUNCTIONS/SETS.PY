'''
Sets are collection of data which are unordered and cannot contain duplications
Index do not work 
'''

num_set = {1,2,3,4,5}

# discard index element by value
num_set.discard(1)

# empty set
num_set.clear()

# add element to a set
num_set.add(6)

# add multiple values to a set
num_set.update([7,8])
